package com.sendev.mooperms.utils;

public class Permissions
{

    /**
     * This string will act as the main permission
     * node throughout the plugin.
     *
     * @var String MAIN
     */
    private static final String MAIN = "mooperms.";

    /**
     * Players with this permission will be allowed
     * to reload the configurations files.
     *
     * @var String RELOAD
     */
    public static final String RELOAD = MAIN + "reload";
}
